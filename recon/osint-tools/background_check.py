def Start(args=None):
    name = ""
    location = None
    
    if args:
        name = args[0]
        if len(args) > 1: location = args[1]
    elif has_db:
        db = DatabaseManagment.get()
        active_profile = db.get("ACTIVE_PROFILE")
        r_host = db.get("R_HOST", "")

        # 1. Try Active Profile first
        if active_profile:
            profiles = DatabaseManagment.getProfiles()
            if active_profile in profiles:
                p_data = profiles[active_profile]
                name = p_data.get("name", active_profile)
                location = p_data.get("geolocation") or p_data.get("address")
        
        # 2. Try resolving R_HOST to a profile
        if not name and r_host:
            profiles = DatabaseManagment.getProfiles()
            for p_name, p_data in profiles.items():
                if p_data.get("ip") == r_host or p_name == r_host:
                    name = p_data.get("name", p_name)
                    location = p_data.get("geolocation") or p_data.get("address")
                    break
            
            # 3. Fallback to R_HOST itself if it's not a raw IP
            if not name and not r_host.replace('.', '').isdigit():
                name = r_host

    if not name or name.replace('.', '').isdigit():
        print("[-] Error: Target name required (e.g., run \"John Doe\" \"New York\")")
        print("[*] Tip: Use 'set profile <Name>' to link an OSINT profile to your session.")
        return

    print(f"[*] Generating Investigative Dossier for: {name}")
    if location and location != "Unknown": 
        print(f"[*] Target Location Context: {location}")

    dork_map = generate_professional_dorks(name, location)

    # Database Synchronization
    if has_db:
        profile = {
            "name": name,
            "geolocation": location or "Unknown",
            "last_audit": datetime.datetime.now().strftime("%Y-%m-%d"),
            "category": "High Value Target"
        }
        DatabaseManagment.addProfile(profile)
        print("[*] Target persona enriched in profiles database.")

    # PDF Generation (Pro Feature)
    if has_pdf:
        if not LicenseManager.check_pro_status():
            print("[*] Skipping Professional Dossier PDF generation (SuperSploit Pro feature).")
        else:
            try:
                pdf = DossierReport()
                pdf.add_page()
                
                pdf.chapter_title("1. TARGET SUMMARY")
                summary = f"Subject Name: {name}\nPrimary Location: {location or 'Not Specified'}\n"
                summary += "Intelligence Level: Multi-Vector Analysis\n"
                summary += f"Report ID: {uuid.uuid4().hex[:8].upper()}"
                pdf.chapter_body(summary)

                for category, dorks in dork_map.items():
                    pdf.chapter_title(category)
                    for d in dorks:
                        pdf.add_dork_link(category.split()[-1], d)
                    pdf.ln(4)

                pdf.chapter_title("6. INVESTIGATIVE NOTES")
                pdf.chapter_body("Data aggregated from public repositories. No active intrusion was performed. All links point to publicly accessible records or search indices.")

                # Save to loot
                install = DatabaseManagment.getInstall() if has_db else "."
                report_dir = os.path.join(install, ".data", ".loot", "reports")
                os.makedirs(report_dir, exist_ok=True)
                filename = f"Dossier_{name.replace(' ', '_')}.pdf"
                report_path = os.path.join(report_dir, filename)
                pdf.output(report_path)
                print(f"[+] Investigative Dossier Exported: {report_path}")
                
            except Exception as e:
                print(f"[-] PDF Export Error: {e}")

    # CLI Output
    print(f"\n[*] Dossier Structure for {name}:")
    for category, dorks in dork_map.items():
        print(f"\n{category}")
        for d in dorks:
            print(f"  - {d}")

if __name__ == "__main__":
    Start(sys.argv[1:])
