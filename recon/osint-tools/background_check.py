class DossierReport(FPDF if has_pdf else object):
    """Custom PDF class for professional dossier formatting."""
    def sanitize_text(self, text):
        """Strips non-latin-1 characters to prevent encoding errors."""
        if not isinstance(text, str): return text
        return text.encode('latin-1', 'ignore').decode('latin-1')

    def header(self):
        if not has_pdf: return
        self.set_font('Arial', 'B', 15)
        self.cell(0, 10, 'CONFIDENTIAL INVESTIGATIVE DOSSIER', 0, 1, 'C')
        self.set_font('Arial', 'I', 8)
        self.cell(0, 5, f'Generated by SuperSploit OSINT Suite - {datetime.datetime.now().strftime("%Y-%m-%d %H:%M")}', 0, 1, 'C')
        self.ln(10)

    def chapter_title(self, title):
        title = self.sanitize_text(title)
        self.set_font('Arial', 'B', 12)
        self.set_fill_color(200, 220, 255)
        self.cell(0, 8, title, 0, 1, 'L', 1)
        self.ln(4)

    def chapter_body(self, body):
        body = self.sanitize_text(body)
        self.set_font('Arial', '', 10)
        self.multi_cell(0, 5, body)
        self.ln()

    def add_dork_link(self, label, query):
        url = f"https://www.google.com/search?q={urllib.parse.quote_plus(query)}"
        self.set_font('Arial', 'B', 9)
        self.cell(40, 6, f"{label}: ", 0, 0)
        self.set_font('Arial', '', 9)
        self.set_text_color(0, 0, 255)
        self.cell(0, 6, query, 0, 1, 'L', False, url)
        self.set_text_color(0, 0, 0)

